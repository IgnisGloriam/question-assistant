def build_generation_prompt(
    context: str, 
    n_questions: int = 5, 
    difficulty: str = "Средний"
) -> str:
    """
    Собирает готовый промпт для генерации вопросов в формате ChatML 
    (стандарт для Qwen2.5 и многих других современных моделей).

    Args:
        context: текст учебного материала (из шага 5)
        n_questions: количество вопросов
        difficulty: уровень сложности (Лёгкий, Средний, Сложный)

    Returns:
        Сформированная строка промпта, готовая для подачи в LLM
    """
    
    # Описание уровней сложности (на основе таксономии Блума)
    difficulty_instructions = {
        "Лёгкий": "Вопросы должны проверять базовое запоминание фактов, дат, имен, терминов и определений (уровень «Знание»).",
        "Средний": "Вопросы должны проверять понимание концепций, причинно-следственные связи и способность объяснить суть процессов (уровень «Понимание»).",
        "Сложный": "Вопросы-задачи. Они должны требовать анализа, сравнения, применения знаний к новым ситуациям или выявления скрытых закономерностей (уровни «Применение» и «Анализ»)."
    }

    # Если передали неизвестную сложность, берем среднюю
    diff_text = difficulty_instructions.get(difficulty, difficulty_instructions["Средний"])

    # Формируем структуру ChatML
    # <|im_start|> и <|im_end|> — это специальные токены модели Qwen2.5,
    # которые помогают ей понимать, где системная инструкция, где запрос пользователя, а где должен быть её ответ.
    
    prompt = f"""<|im_start|>system
Ты — опытный методист и помощник преподавателя. Твоя задача — составлять качественные тестовые вопросы строго на основе предоставленного учебного материала.
Не придумывай факты, которых нет в тексте.<|im_end|>
<|im_start|>user
Составь {n_questions} тестовых вопросов с 4 вариантами ответа на основе материала ниже.

УРОВЕНЬ СЛОЖНОСТИ: {difficulty}
{diff_text}

МАТЕРИАЛ ДЛЯ ВОПРОСОВ:
{context}

ТРЕБОВАНИЯ И ОГРАНИЧЕНИЯ:
1. Каждый вопрос должен проверять одну конкретную и важную идею из материала.
2. Правильный ответ должен быть только один и абсолютно однозначный.
3. Неправильные варианты (дистракторы) должны быть правдоподобными и логичными, чтобы студент, не знающий материала, мог ошибиться.
4. Не используй формулировки типа «Все вышеперечисленное» или «Ничего из вышеперечисленного».
5. Строго опирайся только на предоставленный материал. Если для ответа нужны знания извне — не задавай этот вопрос.

ФОРМАТ ОТВЕТА (строго соблюдай):
**Вопрос N:** [Текст вопроса]
А) [Вариант 1]
Б) [Вариант 2]
В) [Вариант 3]
Г) [Вариант 4]
✅ Правильный ответ: [Буква]
💡 Пояснение: [Краткое объяснение из текста, почему ответ верен]


Ответ должен быть на русском языке!

Начинай генерировать вопросы:<|im_end|>
<|im_start|>assistant
"""
    return prompt


def build_judge_prompt(candidates: list[str], context: str) -> str:
    candidates_block = ""
    for i, candidate in enumerate(candidates, start=1):
        candidates_block += f"\n\n=== ВАРИАНТ {i} ===\n{candidate}\n"

    prompt = f"""<|im_start|>system
Ты — старший эксперт по педагогическому тестированию. Твоя задача — оценивать тестовые вопросы на корректность и качество.<|im_end|>
<|im_start|>user
Ниже представлен исходный учебный материал и несколько вариантов тестовых вопросов, сгенерированных по этому материалу.

ИСХОДНЫЙ МАТЕРИАЛ:
{context[:2000]}

СГЕНЕРИРОВАННЫЕ ВАРИАНТЫ:{candidates_block}

ЗАДАЧА:
1. Внимательно прочитай исходный материал и оцени каждый вариант.
2. Выбери самый лучший вариант, опираясь на критерии:
   - Отсутствие фактических ошибок (правильный ответ действительно правильный согласно тексту).
   - Правдоподобность неправильных ответов.
   - Понятность и четкость формулировок.
3. Если в лучшем варианте есть мелкие недочеты, исправь их (улучши формулировку или замени слабый дистрактор).

Выведи ТОЛЬКО финальный, улучшенный вариант вопросов с ответами в том же формате, без лишних рассуждений. 
Выводить нужно только лучший вариант вопросов с ответами, остальные выводить не нужно

Ответ должен быть на русском языке!
<|im_end|>
<|im_start|>assistant
"""
       
    return prompt



